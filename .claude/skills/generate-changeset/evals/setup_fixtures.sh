#!/usr/bin/env bash
# Materializes the 3 eval fixtures for the generate-changeset skill.
# Each fixture is a minimal git repo with the working tree dirtied to simulate
# the type of change described in evals.json.
#
# Usage: ./setup_fixtures.sh <output_dir>
# Creates: <output_dir>/eval-1/, <output_dir>/eval-2/, <output_dir>/eval-3/

set -euo pipefail

if [[ $# -ne 1 ]]; then
  echo "Usage: $0 <output_dir>" >&2
  exit 1
fi

OUT="$1"
mkdir -p "$OUT"

# Script lives at <repo>/.claude/skills/generate-changeset/evals/setup_fixtures.sh,
# so the repo root is four directories up. Each fixture symlinks the repo's
# node_modules so `npx changeset` resolves without a per-fixture install.
REPO_ROOT="$(cd "$(dirname "$0")/../../../.." && pwd)"

# Helper: create the common scaffolding (package.json, .changeset/, AGENTS.md stub)
init_repo() {
  local dir="$1"
  rm -rf "$dir"
  mkdir -p "$dir/.changeset" "$dir/src/lib/notion" "$dir/src/app/components/atoms" "$dir/src/app/components/molecules"

  cat > "$dir/package.json" <<'EOF'
{
  "name": "notion-as-a-cms",
  "version": "0.3.1",
  "private": true,
  "scripts": {
    "dev": "next dev --turbopack",
    "build": "next build",
    "lint": "next lint"
  },
  "devDependencies": {
    "@changesets/cli": "^2.31.0"
  }
}
EOF

  # Symlink the real project's node_modules so npx can find @changesets/cli
  # without each fixture re-installing the whole tree.
  ln -s "$REPO_ROOT/node_modules" "$dir/node_modules"

  cat > "$dir/.changeset/config.json" <<'EOF'
{
  "$schema": "https://unpkg.com/@changesets/config@3.1.1/schema.json",
  "changelog": "@changesets/cli/changelog",
  "commit": false,
  "fixed": [],
  "linked": [],
  "access": "restricted",
  "baseBranch": "main",
  "updateInternalDependencies": "patch",
  "ignore": []
}
EOF

  cat > "$dir/.changeset/README.md" <<'EOF'
# Changesets

Hello and welcome! This folder has been automatically generated by `@changesets/cli`.
EOF

  # Seed an existing changeset so the skill can see the house style.
  cat > "$dir/.changeset/baseline-dep-bumps.md" <<'EOF'
---
"notion-as-a-cms": patch
---

Refresh patch/minor dependency baseline within current majors: React 19.2, ESLint 9.39, `eslint-config-next` 15.5, `@types/node` 22, plus matching `@types/react*`, `postcss`, `tailwindcss` (3.x), and `typescript` (5.x). No behavior change; clears transitive audit advisories ahead of the upcoming major upgrades (Notion SDK, Next 16, Tailwind 4).
EOF

  # Stub AGENTS.md so the skill (or baseline) can find project context if it looks.
  cat > "$dir/AGENTS.md" <<'EOF'
# AGENTS.md

`notion-as-a-cms` is a Next.js site that uses a Notion database as its CMS.
Versioning is via Changesets. The single package is `notion-as-a-cms`.

The Notion DB schema the code assumes:

| Property          | Type      | Used as                                      |
| ----------------- | --------- | -------------------------------------------- |
| `Name`            | title     | Fallback for <title>                         |
| `Slug`            | rich_text | URL segment                                  |
| `Meta title`      | rich_text | <title>                                      |
| `Meta description`| rich_text | <meta name="description">                    |
| `SEO keywords`    | rich_text | <meta name="keywords">                       |
EOF
}

git_commit_baseline() {
  local dir="$1"
  (
    cd "$dir"
    git init -q -b main
    git config user.email "eval@example.com"
    git config user.name "Eval"
    git add -A
    git commit -q -m "baseline"
  )
}

# ============================================================================
# Eval 1 — Bug fix: add rel="noopener noreferrer" to external links
# ============================================================================
EVAL1="$OUT/eval-1"
init_repo "$EVAL1"

cat > "$EVAL1/src/lib/notion/richText.tsx" <<'EOF'
import type { RichTextItemResponse } from "@notionhq/client/build/src/api-endpoints";

const renderExternalLink = (
  textObj: { href: string; plain_text: string },
  index: number,
) => {
  if (!textObj.href) {
    return null;
  }
  const key = `${textObj.href}-${index}`;
  const href = textObj.href;
  const text = textObj.plain_text;
  return (
    <a key={key} href={href} target="_blank">
      {text}
    </a>
  );
};

export { renderExternalLink };
EOF

git_commit_baseline "$EVAL1"

# Apply the fix (uncommitted)
cat > "$EVAL1/src/lib/notion/richText.tsx" <<'EOF'
import type { RichTextItemResponse } from "@notionhq/client/build/src/api-endpoints";

const renderExternalLink = (
  textObj: { href: string; plain_text: string },
  index: number,
) => {
  if (!textObj.href) {
    return null;
  }
  const key = `${textObj.href}-${index}`;
  const href = textObj.href;
  const text = textObj.plain_text;
  return (
    <a key={key} href={href} target="_blank" rel="noopener noreferrer">
      {text}
    </a>
  );
};

export { renderExternalLink };
EOF

# ============================================================================
# Eval 2 — Feature: add a new Divider atom and wire it into NotionRenderer
# ============================================================================
EVAL2="$OUT/eval-2"
init_repo "$EVAL2"

cat > "$EVAL2/src/app/components/molecules/NotionRenderer.tsx" <<'EOF'
import type { BlockObjectResponse } from "@notionhq/client/build/src/api-endpoints";
import { Paragraph } from "../atoms/Paragraph";
import { Heading1 } from "../atoms/Heading1";

export const NotionRenderer = async ({ blocks }: { blocks: BlockObjectResponse[] }) => {
  return (
    <>
      {blocks.map((block) => {
        switch (block.type) {
          case "paragraph":
            return <Paragraph key={block.id} block={block} />;
          case "heading_1":
            return <Heading1 key={block.id} block={block} />;
          default:
            return null;
        }
      })}
    </>
  );
};
EOF

cat > "$EVAL2/src/app/components/atoms/Paragraph.tsx" <<'EOF'
export const Paragraph = ({ block }: { block: unknown }) => <p>{String(block)}</p>;
EOF

cat > "$EVAL2/src/app/components/atoms/Heading1.tsx" <<'EOF'
export const Heading1 = ({ block }: { block: unknown }) => <h1>{String(block)}</h1>;
EOF

git_commit_baseline "$EVAL2"

# Apply the feature (uncommitted): new Divider atom + dispatcher case
cat > "$EVAL2/src/app/components/atoms/Divider.tsx" <<'EOF'
export const Divider = () => <hr className="my-8 border-notion-divider" />;
EOF

cat > "$EVAL2/src/app/components/molecules/NotionRenderer.tsx" <<'EOF'
import type { BlockObjectResponse } from "@notionhq/client/build/src/api-endpoints";
import { Paragraph } from "../atoms/Paragraph";
import { Heading1 } from "../atoms/Heading1";
import { Divider } from "../atoms/Divider";

export const NotionRenderer = async ({ blocks }: { blocks: BlockObjectResponse[] }) => {
  return (
    <>
      {blocks.map((block) => {
        switch (block.type) {
          case "paragraph":
            return <Paragraph key={block.id} block={block} />;
          case "heading_1":
            return <Heading1 key={block.id} block={block} />;
          case "divider":
            return <Divider key={block.id} />;
          default:
            return null;
        }
      })}
    </>
  );
};
EOF

# ============================================================================
# Eval 3 — Breaking: rename Notion DB property `Meta title` → `SEO title`
# ============================================================================
EVAL3="$OUT/eval-3"
init_repo "$EVAL3"

cat > "$EVAL3/src/lib/notion/page.ts" <<'EOF'
import type { PageObjectResponse } from "@notionhq/client/build/src/api-endpoints";

export const getPageMeta = (page: PageObjectResponse) => {
  const properties = page.properties;
  const title = getPropertyValue(properties["Meta title"]);
  const description = getPropertyValue(properties["Meta description"]);
  const keywords = getPropertyValue(properties["SEO keywords"]);
  return { title, description, keywords };
};

const getPropertyValue = (property: unknown): string => {
  if (!property || typeof property !== "object") return "";
  return "";
};
EOF

git_commit_baseline "$EVAL3"

# Apply the breaking rename (uncommitted)
cat > "$EVAL3/src/lib/notion/page.ts" <<'EOF'
import type { PageObjectResponse } from "@notionhq/client/build/src/api-endpoints";

export const getPageMeta = (page: PageObjectResponse) => {
  const properties = page.properties;
  const title = getPropertyValue(properties["SEO title"]);
  const description = getPropertyValue(properties["Meta description"]);
  const keywords = getPropertyValue(properties["SEO keywords"]);
  return { title, description, keywords };
};

const getPropertyValue = (property: unknown): string => {
  if (!property || typeof property !== "object") return "";
  return "";
};
EOF

echo "Fixtures materialized at $OUT"
